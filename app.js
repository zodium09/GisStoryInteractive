const chapterList = document.querySelector("#chapterList");
const reader = document.querySelector("#reader");
const contents = document.querySelector("#contents");
const readingGuide = document.querySelector("#readingGuide");
const chapterArticle = document.querySelector("#chapterArticle");
const readerChapterNav = document.querySelector("#readerChapterNav");
const progress = document.querySelector("#readingProgress");

function chapterCard(chapter, index) {
  return `
    <article class="chapter-row">
      <button type="button" data-open-chapter="${index}" aria-label="เปิด${chapter.number} ${chapter.title}">
        <span class="chapter-number">${chapter.number}</span>
        <span class="chapter-copy"><strong>${chapter.title}</strong><small>${chapter.summary}</small></span>
        <span class="chapter-arrow" aria-hidden="true">อ่านบทนี้ →</span>
      </button>
      <ol>${chapter.topics.map((topic) => `<li>${topic}</li>`).join("")}</ol>
    </article>`;
}

function renderContents() {
  chapterList.innerHTML = chapters.map(chapterCard).join("");
  readerChapterNav.innerHTML = chapters.map((chapter, index) => `<button type="button" data-open-chapter="${index}"><span>${index + 1}</span>${chapter.title}</button>`).join("");
}

function renderChapter(index) {
  const chapter = chapters[index];
  const previous = index > 0 ? chapters[index - 1] : null;
  const next = index < chapters.length - 1 ? chapters[index + 1] : null;
  document.querySelectorAll("#readerChapterNav button").forEach((button, buttonIndex) => {
    button.classList.toggle("active", buttonIndex === index);
    if (buttonIndex === index) button.setAttribute("aria-current", "page");
    else button.removeAttribute("aria-current");
  });
  chapterArticle.innerHTML = `
    <header class="article-header">
      <p>${chapter.number}</p>
      <h1>${chapter.title}</h1>
      <p class="article-lead">${chapter.summary}</p>
      <figure><img src="${chapter.image}" alt="${chapter.imageAlt}" /><figcaption>ภาพช่วยอ่านกระบวนการของบทนี้ ให้สังเกตความสัมพันธ์ระหว่างพื้นผิว น้ำ อากาศ และสิ่งมีชีวิต</figcaption></figure>
    </header>
    <nav class="on-this-page" aria-label="หัวข้อในบทนี้"><strong>หัวข้อในบทนี้</strong>${chapter.sections.map((section, sectionIndex) => `<a href="#section-${index}-${sectionIndex}">${section.heading}</a>`).join("")}</nav>
    <div class="article-body">
      ${chapter.sections.map((section, sectionIndex) => `<section id="section-${index}-${sectionIndex}"><h2>${section.heading}</h2>${section.paragraphs.map((paragraph) => `<p>${paragraph}</p>`).join("")}</section>`).join("")}
    </div>
    <aside class="chapter-summary"><strong>ใจความสำคัญ</strong><p>${chapter.summary} ทุกหัวข้อในบทนี้จึงควรอ่านเป็นความสัมพันธ์ต่อเนื่อง มากกว่าจำเป็นคำแยกส่วน</p></aside>
    <nav class="chapter-pagination" aria-label="เปลี่ยนบท">
      ${previous ? `<button type="button" data-open-chapter="${index - 1}"><span>บทก่อนหน้า</span>${previous.title}</button>` : `<span></span>`}
      ${next ? `<button type="button" data-open-chapter="${index + 1}"><span>บทถัดไป</span>${next.title}</button>` : `<button type="button" data-close-reader><span>อ่านครบแล้ว</span>กลับสารบัญหลัก</button>`}
    </nav>`;
  reader.hidden = false;
  contents.hidden = true;
  readingGuide.hidden = true;
  document.body.classList.add("reading-mode");
  history.replaceState(null, "", `#chapter-${index + 1}`);
  window.scrollTo({ top: reader.offsetTop - 76, behavior: "smooth" });
}

function closeReader() {
  reader.hidden = true;
  contents.hidden = false;
  readingGuide.hidden = false;
  document.body.classList.remove("reading-mode");
  history.replaceState(null, "", "#contents");
  contents.scrollIntoView({ behavior: "smooth" });
}

document.addEventListener("click", (event) => {
  const opener = event.target.closest("[data-open-chapter]");
  if (opener) renderChapter(Number(opener.dataset.openChapter));
  if (event.target.closest("[data-close-reader], #closeReader")) closeReader();
});

document.querySelector("#themeToggle").addEventListener("click", (event) => {
  document.body.classList.toggle("dark");
  const dark = document.body.classList.contains("dark");
  event.currentTarget.textContent = dark ? "โหมดกลางวัน" : "โหมดกลางคืน";
  localStorage.setItem("textbook-theme", dark ? "dark" : "light");
});

window.addEventListener("scroll", () => {
  const max = document.documentElement.scrollHeight - innerHeight;
  progress.style.width = `${max > 0 ? (scrollY / max) * 100 : 0}%`;
}, { passive: true });

